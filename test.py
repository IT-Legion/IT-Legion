# Запрашиваем у пользователя двузначное число
number = int(input("Введите двузначное число: "))

# Получаем первую и вторую цифры числа
first_digit = number // 10
second_digit = number % 10

# Формируем новое число, переставляя цифры местами
new_number = second_digit * 10 + first_digit

# Выводим результат
print("Число после перестановки цифр:", new_number)